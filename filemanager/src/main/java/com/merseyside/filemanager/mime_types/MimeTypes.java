package com.merseyside.filemanager.mime_types;


public class MimeTypes {

   AudioTypes audioTypes;
   FileTypes fileTypes;
   ImageTypes imageTypes;
   VideoTypes videoTypes;
   TextTypes textTypes;
   PlaylistTypes playlistTypes;

   public ImageTypes getImageTypes() {
      return imageTypes;
   }

   public VideoTypes getVideoTypes() {
      return videoTypes;
   }

   public TextTypes getTextTypes() {
      return textTypes;
   }

   public PlaylistTypes getPlaylistTypes() {
      return playlistTypes;
   }

   public AudioTypes getAudioTypes() {
      return audioTypes;
   }

   public FileTypes getFileTypes() {
      return fileTypes;
   }
}
